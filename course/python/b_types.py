# Strings are text inside quotes (" " or ' ').
# Adding strings together (concatenation) combines them.
print("9" + "10")  # "910"

# Integers (int) are whole numbers.
# Adding integers performs normal math.
print(9 + 9)  # 18

# Floats are numbers with decimals.
# You can add ints and floats together.
print(9 + 9.5)  # 18.5
