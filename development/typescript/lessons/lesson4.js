// Relational or comparison operators

// > - more then
// < - less then
// >= - more then equal
// <= - less then equal

var result = 10 > 5
var result2 = 10 > 11
var result3 = 10 >= 10
var result4 = 9 <= 10

console.log(result)
console.log(result2)
console.log(result3)
console.log(result4)

